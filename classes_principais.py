class ferramentas:
    def setId_ferramentas(self, id_ferramentas):
        self.id_ferramentas = id_ferramentas
    
    def getId_ferramentas(self):
        return self.id_ferramentas 

    def setDescricao_ferramenta(self, descricao_ferramentas):
        self.descricao_ferramentas = descricao_ferramentas
    
    def getDescricao_ferramenta(self):
        return self.descricao_ferramentas 

    def setFabricante(self, fabricante):
        self.fabricante = fabricante
    
    def getFabricante(self):
        return self.fabricante

    def setVoltagem(self, voltagem):
        self.voltagem = voltagem
    
    def getVoltagem(self):
        return self.voltagem

    def setPart_number(self, part_number):
        self.part_number = part_number
    
    def getPart_number(self):
        return self.part_number

    def setTamanho(self, tamanho):
        self.tamanho = tamanho
    
    def getTamanho(self):
        return self.tamanho

    def setTipo(self, tipo):
        self.tipo = tipo
    
    def getTipo(self):
        return self.tipo 

    def setMaterial(self, material):
        self.material = material
    
    def getMaterial(self):
        return self.material
        
    def setMaximo_reserva(self, maximo_reserva):
        self.maximo_reserva = maximo_reserva
    
    def getMaximo_reserva(self):
        return self.maximo_reserva


